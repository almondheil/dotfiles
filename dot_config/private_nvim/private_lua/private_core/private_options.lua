local o = vim.opt

-- turn off compatible mode
o.compatible = false

-- numbering, both types
o.number = true
o.relativenumber = true

-- indentation (tab === 2 spaces by default)
o.tabstop = 2
o.softtabstop = 2
o.shiftwidth = 2
o.expandtab = true

-- disable line wrap
o.wrap = false

-- highlight the current line
o.cursorline = true

-- enable color support
o.termguicolors = true
